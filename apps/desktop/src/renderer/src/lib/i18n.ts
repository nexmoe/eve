type LocaleKey = keyof typeof messages;
export type MessageKey = keyof (typeof messages)["en"];
type MessageParams = Record<string, number | string>;

const resolveLocale = (language: AppLanguage): LocaleKey => {
  if (language === "zh-CN") {
    return "zh";
  }
  if (language === "en-US") {
    return "en";
  }
  if (typeof navigator === "undefined") {
    return "zh";
  }
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
};

export const resolveIntlLocale = (language: AppLanguage): string => {
  const locale = resolveLocale(language);
  return locale === "zh" ? "zh-CN" : "en-US";
};

export const createT = (language: AppLanguage) => {
  const locale = resolveLocale(language);
  return (key: MessageKey, params?: MessageParams): string => {
    const template: string = messages[locale][key];
    if (!params) {
      return template;
    }
    return Object.entries(params).reduce((result, [name, value]) => {
      return result.replaceAll(`{${name}}`, String(value));
    }, template);
  };
};
